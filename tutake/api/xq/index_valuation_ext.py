def query_parameters_ext(self):
    """
    同步历史数据调用的参数
    :return: list(dict)
    """
    max = self.max('trade_date')
    current = pendulum.now(tz="Asia/Shanghai").add(days=-1).format('YYYYMMDD')
    if current == max:
        return []
    return [{"date": current}]


